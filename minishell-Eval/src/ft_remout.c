#include "minishell.h"

char	**ft_remout(char **av)
{
	int	i;

	(void)ft_free(*av);
	i = 0;
	while (*(av + i) && *(av + i + 1))
	{
		if (!*(av + i + 2))
			*(av + i + 1) = (char *)(size_t)ft_free(*(av + i + 1));
		*(av + i) = *(av + i + 1);
		i++;
	}
	return (av);
}
